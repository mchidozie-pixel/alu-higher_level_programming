#!/usr/bin/python3
def common_elements(set_1, set_2):
    """
    Returns a set of elements common to both set_1 and set_2.

    Args:
        set_1 (set): First set of elements.
        set_2 (set): Second set of elements.

    Returns:
        set: Set containing elements found in both sets.
    """
    return set_1 & set_2
